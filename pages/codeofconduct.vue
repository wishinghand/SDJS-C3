      # Code of Conduct

      [San Diego JavaScript Community](http://sandiegojs.org/)(SDJS) is dedicated to providing a welcoming and inclusive experience for everyone. We do not tolerate unacceptable behavior of participants in any form.

      This code of conduct applies to all SDJS spaces, both online and off, including our mailing lists, meetup group, and any location where we are meeting in person. Anyone who violates this code of conduct may be sanctioned or expelled from these spaces at the discretion of the SDJS organizers.

      Some SDJS spaces may have additional rules in place, which will be made clearly available to participants. Participants are responsible for knowing and abiding by these rules.

      # Recommended Conduct

      - **Be welcoming, friendly, and patient.**
      - **Be respectful.** Not all of us will agree all the time, but disagreement is no excuse for poor behavior and poor manners. We might all experience some frustration now and then, but we cannot allow that frustration to turn into a personal attack. It’s important to remember that a community where people feel uncomfortable or threatened is not a productive one. Members of the SDJS community should be respectful when dealing with other members as well as with people outside the SDJS community.
      - **When we disagree, try to understand why.** Disagreements, both social and technical, happen all the time and SDJS is no exception. It is important that we resolve disagreements and differing views constructively. Remember that we’re different. The strength of SDJS comes from its varied community, people from a wide range of backgrounds. Different people have different perspectives on issues. Being unable to understand why someone holds a viewpoint doesn’t mean that they’re wrong. Don’t forget that it is human to err and blaming each other doesn’t get us anywhere, rather offer to help resolving issues and to help learn from mistakes.

      # Unacceptable Behavior

      - Offensive comments related to gender, gender identity and expression, sexual orientation, disability, mental illness, neuro(a)typicality, physical appearance, body size, race, or religion
      - Unwelcome comments regarding a person's lifestyle choices and practices, including those related to food, health, parenting, drugs, and employment.
      - Gratuitous or off-topic sexual images or behavior  in spaces where they're not appropriate
      - Physical contact and simulated physical contact (eg, textual descriptions like "*hug*" or "*backrub*") without consent or after a request to stop.
      - Threats of violence
      - Incitement of violence towards any individual, including encouraging a person to commit suicide or to engage in self-harm
      - Deliberate intimidation
      - Stalking or following
      - Harassing photography or recording, including logging online activity for harassment purposes
      - Unwelcome sexual attention
      - Pattern of inappropriate social contact, such as requesting/assuming inappropriate levels of intimacy with others
      - Continued one-on-one communication after requests to cease
      - Deliberate "outing" of any aspect of a person's identity without their consent except as necessary to protect vulnerable people from intentional abuse
      - Publication of non-harassing private communication

      **SDJS prioritizes marginalized people's safety over privileged people's comfort. SDJS organizers will not act on complaints regarding:**

      - 'Reverse' -isms, including 'reverse racism,' 'reverse sexism,' and 'cisphobia'
      - Reasonable communication of boundaries, such as "leave me alone," "go away," or "I'm not discussing this with you."
      - Communicating in a 'tone' you don't find congenial
      - Criticizing racist, sexist, cissexist, or otherwise oppressive behavior or assumptions

      # Reporting

      If you are being harassed by a member of SDJS, notice that someone else is being harassed, or have any other concerns, please contact the SDJS organizers at sandiegojs-organizers (at) googlegroups (dot) com. If the person who is harassing you is on the team, they will recuse themselves from handling your incident. We will respond as promptly as we can.

      This code of conduct applies to SDJS spaces, but if you are being harassed by a member of SDJS outside our spaces, we still want to know about it. We will take all good-faith reports of unacceptable behavior by SDJS members, especially SDJS organizers, seriously. This includes unacceptable behavior outside our spaces and unacceptable behavior that took place at any point in time. The abuse team reserves the right to exclude people from SDJS based on their past behavior, including behavior outside SDJS spaces and behavior towards people who are not in SDJS.

      In order to protect volunteers from abuse and burnout, we reserve the right to reject any report we believe to have been made in bad faith. Reports intended to silence legitimate criticism may be deleted without response.

      We will respect confidentiality requests for the purpose of protecting victims of abuse. At our discretion, we may publicly name a person about whom we've received harassment complaints, or privately warn third parties about them, if we believe that doing so will increase the safety of SDJS members or the general public. We will not name harassment victims without their affirmative consent.

      # Consequences

      Participants asked to stop any unacceptable behavior are expected to comply immediately.

      If a participant engages in unacceptable behavior, SDJS organizers may take any action they deem appropriate, up to and including expulsion from all SDJS spaces and identification of the participant as a harasser to other SDJS members or the general public.

